// This lovely function will, as a well functioning democracy, divide things in accordance with it's master's plan. Divide a with b.

// sum
const sum = (a, b) => {
  return a + b;
};

// div
const div = (a, b) => {
  return a / b;
};

// mul
const mul = (a, b) => {
  return a * b;
};

// sub
const sub = (a, b) => {
  return a - b;
};

module.exports = {
  sum: sum,
  mul: mul,
  div: div,
  sub: sub
};
